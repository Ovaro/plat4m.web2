package com.le.sunriise.mnyobject.impl;

import com.le.sunriise.mnyobject.FinancialInstitution;
import java.util.Date;

public class FinancialInstitutionImpl implements FinancialInstitution {

    private Integer id;

    private String name;

    private String comment;

    private Date serialDate;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public Date getSerialDate() {
        return serialDate;
    }

    public void setSerialDate(Date serialDate) {
        this.serialDate = serialDate;
    }
}
